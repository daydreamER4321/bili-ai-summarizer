"""实体提取模板：从视频中提取人名、书名、工具、项目、URL等实体。"""

from __future__ import annotations

from . import PromptTemplate, TemplateType, register_template


class EntityTemplate:
    """实体提取模板——信息搜集专用。"""

    def chunk_prompt(self, text: str, time_range: str) -> str:
        return f"""请从以下视频片段中提取所有有价值的实体信息。

时间范围：{time_range}

字幕文本：
{text}

请按以下分类提取，没有的类别留空：

**人物**：（提到的人名、网名、角色名，标注身份/头衔）
- 姓名（身份）

**书籍/论文**：（提到的书名、论文名、作者）
- 《书名》作者

**工具/软件/项目**：（提到的工具、框架、开源项目、网站，标注用途）
- 名称：用途

**关键数据**：（具体的数字、统计、指标，附上下文）
- 数据点（出处/上下文）

**URL/链接**：（提到的网址、GitHub链接、DOI等）
- URL 描述

**概念/术语**：（专业术语、缩写、新概念，附简要解释）
- 术语：简要解释

**事件/日期**：（提到的具体事件、时间节点）
- 日期 事件描述

要求：
- 只提取字幕中明确提到的信息，不要推测
- 每个实体尽量保留原始表述
- 标注该实体出现的时间上下文"""

    def global_prompt(self, chunk_summaries: str, video_title: str) -> str:
        return f"""请基于以下各片段提取的实体，整合生成完整的实体清单。

视频标题：{video_title}

各片段提取结果：
{chunk_summaries}

请按以下格式输出：

## 📋 视频实体清单

### 人物
| 姓名 | 身份/头衔 | 出现位置 |
|------|----------|---------|

### 书籍/论文
| 名称 | 作者 | 出现位置 |
|------|------|---------|

### 工具/软件/项目
| 名称 | 用途 | 链接(如有) | 出现位置 |
|------|------|-----------|---------|

### 关键数据
| 数据 | 上下文 | 出处时间 |
|------|--------|---------|

### 概念/术语
| 术语 | 解释 | 出处时间 |
|------|------|---------|

### 事件/日期
| 日期 | 事件 | 出处时间 |
|------|------|---------|

要求：
- 去重：同一实体在不同片段出现时合并
- 合并后的"出现位置"标注所有相关时间戳
- 按重要性/出现频率排序
- 没有的分类直接省略"""


register_template(TemplateType.ENTITY, EntityTemplate())
