"""观点批判模板：分析视频中的观点立场、逻辑结构、论证强度和潜在偏见。"""

from __future__ import annotations

from . import PromptTemplate, TemplateType, register_template


class CritiqueTemplate:
    """观点批判模板——批判性分析专用。"""

    def chunk_prompt(self, text: str, time_range: str) -> str:
        return f"""请对以下视频片段中的观点进行批判性分析。

时间范围：{time_range}

字幕文本：
{text}

请按以下维度分析：

**核心论点**：（这段话的主要观点是什么？用1-2句话概括）

**论据结构**：
- 事实论据：（引用的具体数据、案例、引用）
- 逻辑推论：（从事实到结论的推理过程）
- 情感论据：（诉诸情绪、价值观的部分）

**逻辑评估**：
- ✅ 有效推理：（哪些推理是有说服力的，为什么）
- ❌ 逻辑漏洞：（识别逻辑谬误，标注类型）
  - 类型（如：以偏概全/滑坡谬误/稻草人/诉诸权威/虚假二分/事后归因等）：具体内容
- ⚠️ 未验证断言：（作为事实陈述但无证据支撑的观点）

**立场与偏见**：
- 明确立场：（作者站在什么立场）
- 潜在利益相关：（是否有未披露的利益关联）
- 选择性呈现：（是否只呈现了一面的证据，省略了什么）

**情绪手法**：（是否使用了煽动性语言、标签化、极端化等修辞）"""

    def global_prompt(self, chunk_summaries: str, video_title: str) -> str:
        return f"""请基于以下各片段的批判性分析，生成整部视频的完整观点评估报告。

视频标题：{video_title}

各片段分析：
{chunk_summaries}

请按以下格式输出：

## 📋 观点批判报告

### 总体立场
用2-3句话概括视频的核心立场和倾向性。

### 核心论点清单
| # | 论点 | 论据强度 | 逻辑健康度 |
|---|------|---------|-----------|
（论据强度：强/中/弱；逻辑健康度：✅健康 / ⚠有瑕疵 / ❌有谬误）

### 逻辑谬误清单
| 时间位置 | 谬误类型 | 原文摘要 | 问题说明 |
|---------|---------|---------|---------|

### 未验证断言
| 断言内容 | 出处时间 | 需要什么证据才能验证 |
|---------|---------|-------------------|

### 偏见评估
- **立场倾向**：（整体偏向哪方）
- **省略信息**：（视频故意或无意忽略了哪些重要信息）
- **情绪操纵**：（使用了哪些情绪手法影响判断）
- **如果要让这个视频更客观，应该补充**：...

### 可取之处
- 视频中有哪些论证是有效的、值得采信的

### 综合评分
- 论据充分度：⭐⭐⭐☆☆（1-5）
- 逻辑严密度：⭐⭐⭐☆☆
- 客观公正度：⭐⭐⭐☆☆
- 信息价值度：⭐⭐⭐☆☆

要求：
- 批判要基于事实和逻辑，不要为了批判而批判
- 即使观点有偏颇，也要承认其中合理的部分
- 未验证 ≠ 错误，只是需要进一步查证
- 评分要给出具体理由"""


register_template(TemplateType.CRITIQUE, CritiqueTemplate())
